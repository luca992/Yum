- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    DWMVenuesTableViewController *tableViewController = segue.destinationViewController;
    UITableViewCell *cell = (UITableViewCell*)sender;
    tableViewController.navigationItem.title=cell.textLabel.text;
    tableViewController.useStoredData=true;
    NSIndexPath *indexPath = [self.tableView indexPathForCell:cell];
    tableViewController.storedAllVenues=[NSKeyedUnarchiver unarchiveObjectWithData:[[_fetchedResultsController objectAtIndexPath:indexPath] valueForKey:@"searchData"]];


